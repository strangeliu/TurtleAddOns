function lib:GetEnchantSpellID(ItemLink)
    local enchant = tonumber(string.match(ItemLink, "item:%d+:(%d+):"))

    local _, _, vItemCode = strfind(ItemLink, "item:(%d+):(%d+):(%d+):"); 
    local _, _, _, _, _, _, _, itemEquip = GetItemInfo(vItemCode) -- 获取 装备类型
    if (enchant and EnchantSpellDBEx[enchant]) then -- 有附魔 并且主目录不存在
        local enchantItemID = EnchantSpellDBEx[enchant] -- 副目录校验
        if (enchantItemID ~= nil) then
            return EnchantSpellDBEx[enchant][itemEquip], enchant
        end
    end
    if (enchant and EnchantSpellDB[enchant]) then
        return EnchantSpellDB[enchant], enchant
    end
    return nil,nil, enchant
end

function lib:GetEnchantItemID(ItemLink)
    local enchant = tonumber(string.match(ItemLink, "item:%d+:(%d+):"))

    local _, _, vItemCode = strfind(ItemLink, "item:(%d+):(%d+):(%d+):"); 
    local _, _, _, _, _, _, _, itemEquip = GetItemInfo(vItemCode) -- 获取 装备类型
    if enchant == 1483 and itemEquip == "INVTYPE_CHEST" then return end --1483有三个部位，头部和腿部为次级冥想秘药，胸为附魔胸甲 - 优异法力
    if enchant == 3011 and (itemEquip == "INVTYPE_WRIST" or itemEquip == "INVTYPE_FEET") then return end --3011有四个部位，项链和戒指为炽热红宝石，护腕和脚部为附魔 - 吸血鬼
    if enchant == 928 and itemEquip == "INVTYPE_CHEST" then return end --928有三个部位，项链和戒指为惊艳帝王石，胸部为附魔 - 状态

    if (enchant and EnchantItemDBEx[enchant]) then -- 有附魔 并且主目录不存在
        local enchantItemID = EnchantItemDBEx[enchant] -- 副目录校验
        if (enchantItemID ~= nil) then
            return EnchantItemDBEx[enchant][itemEquip], enchant
        end
    end
    if (enchant and EnchantItemDB[enchant]) then
        return EnchantItemDB[enchant], enchant
    end
    return nil, enchant
end
